#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const servicesDir = path.join(__dirname, '..', 'src', 'app', 'shared', 'services', 'BackendServices');
const outFile = path.join(servicesDir, 'index.ts');

function walkFiles(dir) {
  const results = [];
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    const full = path.join(dir, e.name);
    if (e.isFile()) {
      results.push(full);
    } else if (e.isDirectory()) {
      results.push(...walkFiles(full));
    }
  }
  return results;
}

function extractExportedClasses(src) {
  const re = /export\s+class\s+([A-Za-z0-9_]+)/g;
  const matches = [];
  let m;
  while ((m = re.exec(src))) matches.push(m[1]);
  return matches;
}

try {
  if (!fs.existsSync(servicesDir)) throw new Error('BackendServices folder not found: ' + servicesDir);
  const files = walkFiles(servicesDir).filter(f => /\.service(\.ts|\.js)$/i.test(f));
  const classToFile = new Map(); // className -> file (no ext)
  const fileToClasses = new Map(); // file -> [classNames]

  for (const f of files) {
    const rel = './' + path.relative(servicesDir, f).replace(/\\/g, '/');
    const relNoExt = rel.replace(/\.(ts|js)$/, '');
    const src = fs.readFileSync(f, 'utf8');
    const classes = extractExportedClasses(src);
    if (classes.length === 0) continue;
    for (const c of classes) {
      if (!classToFile.has(c)) {
        classToFile.set(c, relNoExt);
        if (!fileToClasses.has(relNoExt)) fileToClasses.set(relNoExt, []);
        fileToClasses.get(relNoExt).push(c);
      } else {
        const existing = classToFile.get(c);
        if (existing !== relNoExt) {
          console.warn('Duplicate exported class', c, 'found in', existing, 'and', relNoExt, '- keeping', existing);
        }
      }
    }
  }

  const imports = [];
  for (const [file, classes] of fileToClasses) {
    imports.push(`import { ${classes.join(', ')} } from '${file}';`);
  }

  const controllerClasses = Array.from(classToFile.keys()).filter(c => /Controller$/.test(c));

  // Always ensure array exists
  const content = [];
  content.push('// Auto-generated by tools/generate-backendservices-index.js');
  content.push('// Do not edit by hand. This file aggregates controller classes for runtime discovery.');
  content.push('');
  if (imports.length) content.push(...imports);
  content.push('');
  content.push('export const CONTROLLER_CLASSES: any[] = [');
  for (const c of controllerClasses) content.push(`  ${c},`);
  content.push('];');
  content.push('');
  // Also export each controller symbol for explicit imports
  for (const c of controllerClasses) content.push(`export { ${c} };`);
  content.push('');

  fs.writeFileSync(outFile, content.join('\n'), 'utf8');
  console.log('Wrote', outFile);
} catch (e) {
  console.error('Error:', e && e.message || e);
  process.exit(2);
}
